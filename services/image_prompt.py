"""Сборка запроса к LLM: из свободного описания — готовый промпт для генерации изображения."""


def build_image_prompt_request(user_idea: str) -> str:
    idea = (user_idea or "").strip()
    return (
        "Ты эксперт по промптам для нейросетевой генерации изображений (Stable Diffusion, Midjourney, "
        "DALL-E, Imagen и т.п.).\n\n"
        "Пользователь своими словами описывает, какую картинку хочет получить. "
        "Твоя задача — превратить это в один сильный промпт для генератора.\n\n"
        "Сформируй ответ строго в таком формате (без лишних преамбул до и после):\n"
        "1) Строка «EN:» и далее один сплошной промпт на английском (ключевые слова через запятую "
        "или короткие фразы): сцена, свет, стиль, камера, детали, настроение.\n"
        "2) Пустая строка.\n"
        "3) Строка «RU:» и одно короткое пояснение по-русски (1–2 предложения), что заложено в EN-промпт.\n\n"
        "Если в описании пользователя есть особые требования (например конкретный жанр, «как в рекламе», "
        "«сохранить лицо с референса» и т.д.) — отрази их в EN. Не добавляй от себя то, чего пользователь не просил.\n"
        "Если деталей мало — аккуратно дострой типичные вещи (освещение, ракурс), не противореча исходному смыслу.\n\n"
        "---\n"
        f"Описание от пользователя:\n{idea}"
    )
